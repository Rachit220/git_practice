# Create variables of different types
an_integer = 42                 # Integer type
a_float = 3.14159               # Floating-point type
a_string = "Hello, World!"      # String type
a_boolean = True                # Boolean type
a_list = [1, 2, 3, 4]           # List type (similar to an array)
a_dictionary = {'key': 'value', 'number': 100} # Dictionary type (key-value pairs)

# Print the value and type of each variable
print(f"Variable: {an_integer}, Type: {type(an_integer)}")
print(f"Variable: {a_float}, Type: {type(a_float)}")
print(f"Variable: {a_string}, Type: {type(a_string)}")
print(f"Variable: {a_boolean}, Type: {type(a_boolean)}")
print(f"Variable: {a_list}, Type: {type(a_list)}")
print(f"Variable: {a_dictionary}, Type: {type(a_dictionary)}")
