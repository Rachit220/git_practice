# Storing personal information using different data types

# String
name = "Rachit Prajapati"

# Integer
age = 20

# Float
height = 5.8

# Boolean
is_student = True

# List
skills = ["Python", "Git", "Problem Solving"]

# Dictionary
address = {
    "city": "Ahmedabad",
    "state": "Gujarat",
    "country": "India"
}

# Displaying the information
print("----- Personal Information -----")
print("Name:", name)
print("Age:", age)
print("Height:", height, "feet")
print("Student:", is_student)
print("Skills:", skills)
print("City:", address["city"])
print("State:", address["state"])
print("Country:", address["country"])
